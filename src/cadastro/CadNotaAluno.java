    private void cadMatricAlunoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cadMatricAlunoFocusLost
        // Aqui tem que vim um inner join, de [aluno] e [notaAluno]
        String sql = "";
        try {
            if(!cadMatricAluno.getText().equals("")){
                sql = "select a.matricula, a.nome, nA.nota1, nA.nota2, nA.notaExercicio, nA.provaFinal from aluno as a left join notaAluno as nA on a.matricula = nA.matricula where a.matricula = ?";
                conn.conectar();
                conn.sttm = conn.con.prepareStatement(sql);
                conn.sttm.setInt(1, Integer.parseInt(cadMatricAluno.getText()));
                ResultSet rs = conn.sttm.executeQuery();
                if(rs.next()){
                    cadMatricAluno.setText(rs.getString("matricula"));
                    cadNomeAluno.setText(rs.getString("nome"));
                    cadAlunoNota1.setText(rs.getString("nota1"));
                    cadAlunoNota2.setText(rs.getString("nota2"));
                    cadAlunoNotaExer.setText(rs.getString("notaExercicio"));
                    cadAlunoNotaPF.setText(rs.getString("provaFinal"));
                }else{
                    cadMatricAluno.setText("NÃO EXISTE");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }finally{
            try {
                conn.con.close();
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_cadMatricAlunoFocusLost

