    private void butCalcMediaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butCalcMediaActionPerformed
        // TODO add your handling code here:
        double media = (Double.parseDouble(this.cadAlunoNota1.getText()) + Double.parseDouble(this.cadAlunoNota2.getText()) 
                + Double.parseDouble(this.cadAlunoNotaExer.getText())) / 3;
        double provaFinal = Double.parseDouble(this.cadAlunoNotaPF.getText());
        if(media >= 7){
            cadAlunMedFinal.setText(Double.toString(media));
        }else{
            if(provaFinal > media){
                media = provaFinal;
            }
            cadAlunMedFinal.setText(Double.toString(media));
        }
    }//GEN-LAST:event_butCalcMediaActionPerformed

